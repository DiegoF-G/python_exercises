from math import sin, cos, tan, radians, degrees
theta = radians(float(input('Digite um ângulo em graus: ')))
print(f'Seno de {degrees(theta)}: {sin(theta):.2f}\nCosseno de {degrees(theta)}: {cos(theta):.2f}')
print(f'Tangente de {degrees(theta)}: {tan(theta):.2f}')
